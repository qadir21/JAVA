package Basic;
public class DivisionOfDataType
{
    public static void main(String[] args)
    {
        double x = 5 / 2;
        System.out.println(x);
        x = 5.0 / 2;
        System.out.println(x);
        x = 5 / 2.0;
        System.out.println(x);
        x = 5.0 / 2.0;
        System.out.println(x);
    }
}