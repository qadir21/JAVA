package Basic;

public class ArithmaticOperationOnInt
{
    public static void main(String[] args)
    {
       int x = 5, y = 2;
        System.out.println(x - y);
        System.out.println(x + y);
        System.out.println(x * y);
        System.out.println(x / y);
        double a = 39;
        double b = 10;
        System.out.println();
        System.out.println(a - b);
        System.out.println(a + b);
        System.out.println(a * b);
        System.out.println(a / b);
        // isko comment bolte hai
        System.out.println("\nComment............");
        System.out.println(69 + 1);
        System.out.println("69 + 1");
        System.out.println("\nArea of circle");
        int r = 5;
        System.out.println((3.141592 * r * r));
        r = 9;
        System.out.println((3.141592 * r * r));
        System.out.println();
        double p = 8;
        double q = 3.141592 * p * p;
        System.out.println(q);
    }
}