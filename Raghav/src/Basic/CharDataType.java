package Basic;

public class CharDataType
{
    public static void main(String[] args)
    {
        char nikita = 'r';
        System.out.println(nikita);
        char eva = '%';
        System.out.println(eva);
        int x = 'a';
        System.out.println(x);
        char c = (char)97.0;
        System.out.println(c);
        char b = '$';
        System.out.println(b + 0);
    }
}